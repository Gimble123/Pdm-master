package com.unisc.pdm;

public class Rgb {

    private String R;
    private String G;
    private String B;
    private String nomes;

    public String getNomes() {
        return nomes;
    }

    public void setNomes(String nomes) {
        this.nomes = nomes;
    }

    public String getR() {
        return R;
    }

    public void setR(String r) {
        R = r;
    }

    public String getG() {
        return G;
    }

    public void setG(String g) {
        G = g;
    }

    public String getB() {
        return B;
    }

    public void setB(String b) {
        B = b;
    }
}
